package org.matsim.contrib.freight.carrier;

public class TimeWindow {
	
	private double start;
	
	private double end;
	
	public TimeWindow(double start, double end) {
		this.start = start;
		this.end = end;
	}

	public double getStart() {
		return start;
	}

	public double getEnd() {
		return end;
	}
	
}