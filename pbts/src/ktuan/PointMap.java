package ktuan;

public class PointMap {
	public int _id;
	public double _tungDo;
	public double _hoanhDo;
	public PointMap(int id, double tungDo, double hoanhDo) {
		this._id= id;
		this._tungDo= tungDo;
		this._hoanhDo= hoanhDo;
	}
	public String toString(){
		return String.format("PointMap[id= %10d,tungDo= %10.2f,hoanhDo= %10.2f]",_id,_tungDo,_hoanhDo);
	}
}
